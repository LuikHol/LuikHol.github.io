// Defina a URL do seu Infura Project ID e o endereço do contrato MangooToken
const infuraUrl = 'https://sepolia.infura.io/v3/086a1ca2525b4a4b861911373cb7216a';
const contratoEndereco = '0x58d459CFa5bFB9c94b7FD8102D8dC1480928E6B7'; // Endereço do contrato MangooToken

async function obterSaldoMangoo() {
    const enderecoCarteira = '0x48A9D2f0F6e6aABaBbE5C51990a1B532F40314c0';
    const web3 = new Web3(infuraUrl);

    const contrato = new web3.eth.Contract(contratoABI, contratoEndereco);
    const saldoMangoo = await contrato.methods.balanceOf(enderecoCarteira).call();

    // Exibir os resultados no HTML
    document.getElementById('endereco-carteira').textContent = enderecoCarteira;
    document.getElementById('saldo-mangoo').textContent = saldoMangoo + ' MGO'; // Adicione 'MGO' ao saldo
}

// Aguardar o carregamento do DOM e então chamar a função
document.addEventListener('DOMContentLoaded', () => {
    obterSaldoMangoo();
});